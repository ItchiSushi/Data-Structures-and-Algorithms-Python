# List is not optimal for manipulating specific data
user = ["Daniel", "Marais", 25]

print(user[0])
print(user[2])
# Hash Tables(Dictionaries) split into Key and Value is more beneficial as the data is easy to access. This uses hash function
user = {"first_name": "Mary-Beth",
        "middle_name": "Gabriella",
        "last_name": "Agulhas",
        "age": 24}
print(user["first_name"])
print(user["age"])